package profundidad3;

public class Numero {
	
	private String letra;
	private int numero;
	
	public Numero(String letra, int numero) {
		super();
		this.letra = letra;
		this.numero = numero;
	}
	
	public Numero(){
		
	}

	public String getLetra() {
		return letra;
	}

	public void setLetra(String letra) {
		this.letra = letra;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
	
	

}
